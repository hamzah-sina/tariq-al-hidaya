class HadithItem {
  final int hadithNumber;
  final String text;
  final int bookIndex;

  const HadithItem({
    required this.hadithNumber,
    required this.text,
    required this.bookIndex,
  });

  factory HadithItem.fromJson(Map<String, dynamic> json) {
    return HadithItem(
      hadithNumber: json['hadithnumber'] as int,
      text: (json['text'] as String?) ?? '',
      bookIndex: (json['reference']?['book'] as int?) ?? 0,
    );
  }
}

